CREATE TABLE hikitori_data (
    id INT(11) NOT NULL AUTO_INCREMENT,
    date_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    name_hikitori VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    actual_pouling VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    loading_time VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    status VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    cycle_normal VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    andon VARCHAR(50) COLLATE utf8mb4_general_ci DEFAULT NULL,
    PRIMARY KEY (id)
) ENGINE=InnoDB 
DEFAULT CHARSET=utf8mb4 
COLLATE=utf8mb4_general_ci;

